# Exercício 3 - Avançado:

# Crie uma classe chamada Cafeteira que simula o funcionamento de uma cafeteira. Ela deve ter os seguintes atributos:

# Um atributo público chamado água.
# Um atributo protegido chamado _café.
# Um atributo privado chamado __copos_preparados.
# Implemente métodos para encher a cafeteira com água, adicionar café, preparar uma xícara de café e contar quantas xícaras já foram preparadas. Certifique-se de que a água e o café só possam ser acessados internamente pela classe e que o número de copos preparados seja apenas lido, não modificado diretamente. Garanta que, ao preparar uma xícara de café, a quantidade de água e café seja reduzida adequadamente.
